function add (x, y) {
const result =  x + y;
return result;
}

function subtract (x, y) {
const result =  x - y;
return result;
}

function multiply (x, y) {
const result =  x * y;
return result;
}

function divide (x, y) {
const result =  x / y;
return result;
}

module.exports = {
  add: add,
  subtract: subtract,
  multiply: multiply,
  divide: divide
};
